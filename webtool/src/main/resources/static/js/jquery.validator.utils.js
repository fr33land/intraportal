jQuery.validator.addMethod("validpassword", function (value, element) {
    if (/^(?=.*\d)(?=.*[a-z])(?=.*[A-Z]).{6,64}$/.test(value)) {
        return true;
    } else {
        return false;
    }
}, "Password does not meet required complexity");

jQuery.validator.addMethod("validusername", function (value, element) {
    if (/^[a-zA-Z0-9]*$/.test(value)) {
        return true;
    } else {
        return false;
    }
}, "Username must contain only alphanumeric characters (a-z 0-9)");